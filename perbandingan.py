print("ini adalah contoh operator perbandingan")
# > lebih dari
# < kurang dari
# >= lebih dari sama dengan
# <= kurang dari sama dengan
# == sama dengan
# != tidak sama dengan
print(1>1)
# ini akan menghasilkan False karena 1 tidak lebih dari 1
print(1<1)
# ini akan menghasilkan False karena 1 tidak kurang dari 1
print(1>=1)
# ini akan menghasilkan True karena 1 sama dengan 1
print(1<=1)
# ini akan menghasilkan True karena 1 sama dengan 1
print(1==1)
# ini akan menghasilkan True karena 1 sama dengan 1
print(1==10)
# ini akan menghasilkan False karena 1 tidak sama dengan 10

