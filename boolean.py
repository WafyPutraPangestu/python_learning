# operasi logika
# and, or, not
print(True and True)
print(True and False)
print(False and True)
print(False and False)
# kalo and, salah satu false maka hasilnya false kalo semua true maka hasilnya true

print(True or True)
print(True or False)
print(False or True)
print(False or False)
# kalo or, salah satu true maka hasilnya true kalo semua false maka hasilnya false

print(not True)
print(not False)
# kalo not, true jadi false dan false jadi true